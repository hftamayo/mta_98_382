function mostrarFunciones(cifra){
    let dato = cifra;
    //PI
    Math.PI;
    //redondear decimales
    Math.round(dato);
    //potenciacion, acepta params x, y
    Math.pow(cifra, 2);
    //raiz cuadrada
    Math.sqrt(cifra);
    //devuelve el valor absoluto (positivo)
    Math.abs(cifra);
    //devuelve el maximo numero posible a redondear
    Math.ceil(cifra);
    //devuelve el minimo numero posible a redondear
    Math.floor(cifra);
    //funcion seno
    Math.sin(cifra);
    //funcion coseno
    Math.cos(cifra);
    //generacion de numero aleatorio
    Math.random();    
    
    
}
